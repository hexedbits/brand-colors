import UIKit

extension UIColor {
    public enum HexedBits {
        public static let red = UIColor(hue: 359.0/360.0, saturation: 0.9, brightness: 0.88, alpha: 1.0)

        public static let brightRed = UIColor(hue: 359.0/360.0, saturation: 0.66, brightness: 0.94, alpha: 1.0)

        public static let darkRed = UIColor(hue: 358.0/360.0, saturation: 0.9, brightness: 0.33, alpha: 1.0)

        public static let white = UIColor(hue: 0.0, saturation: 0.03, brightness: 0.9, alpha: 1.0)

        public static let black = UIColor(hue: 0.0, saturation: 0.22, brightness: 0.14, alpha: 1.0)
    }
}
